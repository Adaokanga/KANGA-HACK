def resolver_ips(arquivo_entrada, arquivo_saida):
    try:
        with open(arquivo_entrada, 'r') as file:
            ips = file.readlines()
        
        dominios = set()  # Usamos um conjunto para evitar duplicatas
        
        for ip in ips:
            ip = ip.strip()
            if ip:
                try:
                    dominio = socket.gethostbyaddr(ip)[0]
                    dominios.add(dominio)
                    print(f"{verde}[‚úĒ] {ip} -> {dominio}{reset}")
                except socket.herror:
                    print(f"{vermelho}[‚úĖ] {ip} -> N√£o foi poss√≠vel resolver{reset}")
        
        # Salvar apenas os dom√≠nios positivos ordenados
        with open(arquivo_saida, 'w') as output_file:
            for dominio in sorted(dominios):
                output_file.write(f"{dominio}\n")
        
        print(f"\n{verde}[‚úĒ] Resultados salvos em: {arquivo_saida}{reset}")
    
    except FileNotFoundError:
        print(f"{vermelho}[!] Arquivo '{arquivo_entrada}' n√£o encontrado!{reset}")

